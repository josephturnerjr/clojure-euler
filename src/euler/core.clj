(defn pow
    [x p]
    (reduce * (repeat p x)))
(defn square
    [x]
    (pow x 2))
(defn sum
    [coll]
    (apply + coll))
(defn product
    [coll]
    (apply * coll))
(defn gcd
    [x y]
    (if (= y 0)
        x
        (recur y (mod x y))))

(def lazy-fib
    (map first
        (iterate
            (fn [[a b]] [b (+ a b)]) [0N 1N])))

(defn digits
    [n]
    (count (str n)))

(defn get-digits
    [n]
    (map #(Integer/parseInt (str %)) (seq (str n))))

(defn is-palindrome?
    [n]
    (def str-n (seq (str n)))
    (= str-n (reverse str-n)))

(defn divisor?
    [c n]
    (if (== c 1)
        false
        (= 0 (mod n c))))

(defn factorial
    [n]
    (defn fact-iter
        [n acc]
        (if (= n 1)
            acc
            (recur (- n 1) (* acc n))))
    (fact-iter n 1N))

(defn any-divisor?
    [coll n]
    (some #(divisor? % n) coll))

(def lazy-primes
    (map #(last (first %))
        (drop 1 (iterate
            (fn [[primes coll]]
                [
                    (conj primes (first coll))
                    (drop-while
                        #(any-divisor? primes %)
                        (rest coll))
                ]) 
            [[] (drop 2 (range))]))))

(def lazy-primes-so
    (map #(first %)
        (drop 1
            (iterate
                (fn [[prime coll]]
                    [
                        (first coll)
                        (filter
                            #(not (divisor? (first coll) %))
                            (rest coll))
                    ]) 
                [2 (drop 3 (range))]))))

(defn sieve
    [maximum]
    (def candidates (apply sorted-set (range 3 maximum 2)))
    (defn get-primes
        [candidates primes]
        (if (empty? candidates)
            primes
            (let [prime (first candidates)]
                (recur (difference candidates (set (range prime maximum prime))) (conj primes prime)))))
    (get-primes candidates [2]))
    

(defn naive-factor
    [n]
    (if (= n 1)
        nil
        (let [factor (first (drop-while #(not (divisor? % n)) (drop-while #(< % 2) (range))))]
            (conj (naive-factor (/ n factor)) factor))))

(defn factors
    [n]
    (set (naive-factor n)))

(defn totient
    [n]
    (* n
        (product
            (map #(- 1 (/ 1 %)) (factors n)))))

